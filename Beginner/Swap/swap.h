#ifndef SWAP_H
#define SWAP_H

// Function that swaps two integers using arithmetic operations
void arithmetic_swap(int *a, int *b);

// Function that swaps two integers using bitwise XOR operations
void bitwise_swap(int *a, int *b);

// Convenience function that swaps two integers using a temporary variable
void temp_swap(int *a, int *b);

#endif // SWAP_H